print("Demonstration of Sequence data types")

# List []
ldata = [10,20,30,40]

# Tuple ()
tdata = (10,20,30,40)

# Set {}
sdata = {10,20,30,40}

# Dictionary {"Key1":Value1, "Key2":Value2, "Key3":Value3,..."KeyN":ValueN} (Like Hash Table/Hash Map)
ddata = {"C Programming":400, "C++ Programming":550, "Java Programming":900, "Python Programming": 890}

print(ldata)
print(tdata)
print(sdata)
print(ddata)

print(type(ldata))
print(type(tdata))
print(type(sdata))
print(type(ddata))